public class Main {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        /**
         * Represents the shorter side of a rectangle.
         */
        int minorSide = 5;
        /**
         * Represents the longest side of a rectangle.
         */
        int majorSide = 10;
        /**
         * Equation to calculate the area of a rectangle.
         */
        double areaRectangle = majorSide * minorSide;
        System.out.println ("The area of the rectangle is: " + areaRectangle);

    }
    
}

